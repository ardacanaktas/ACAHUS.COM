    <div class="comtainerbaş">ATAK T-129</div>
        <p>T129 ATAK Helikopteri, Türk Silahlı Kuvvetlerinin taarruz helikopteri ihtiyacını karşılamak amacıyla Türkiye'ye özgü millî kabiliyetler kullanılarak geliştirilmiştir.
            7 Eylül 2007 tarihinde Savunma Sanayi Başkanlığı ile imzalanan sözleşme çerçevesinde TUSAŞ, ana yüklenici olarak platform geliştirme, sistem entegrasyonu, test ve doğrulama, seri üretim ve ömür devri lojistik desteği sorumluluğunu üstlenmiştir.
            T129 ATAK Helikopterinin performansı zorlu "sıcak hava-yüksek irtifa" görevleri için optimize edilmiş olup, gece ve gündüz koşullarında yüksek manevra ve performans kabiliyeti ile Türk Silahlı Kuvvetlerinin operasyonlarında etkin bir şekilde görev yapmaktadır.          
            T129 ATAK Helikopteri ilk uçuşunu 17 Ağustos 2011 tarihinde gerçekleştirmiş, test ve kalifikasyon süreçlerinin ardından ilk teslimat 22 Nisan 2014 tarihinde Kara Kuvvetleri Komutanlığına yapılmıştır. Aralık 2023 itibarı ile Kara Kuvvetleri Komutanlığı, İçişleri Bakanlığı Jandarma Genel Komutanlığı, İçişleri Bakanlığı Emniyet Genel Müdürlüğü, Filipinler Hava Kuvvetleri ve Nijerya Hava Kuvvetlerine toplam 82 helikopter teslimatı gerçekleştirilmiştir          
            ATAK Programı kapsamında, Kara Kuvvetleri Komutanlığına 59’u kesin 17’si opsiyonel toplam 76 adet, İçişleri Bakanlığı’na 24’ü kesin 3’ü opsiyonel toplam 27 adet, Filipinler Hava Kuvvetlerine 6 adet, Nijerya Hava Kuvvetlerine 6 adet T129 ATAK Helikopteri teslim edilecektir.            
            </p>
